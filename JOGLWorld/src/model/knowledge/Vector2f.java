package model.knowledge;

public class Vector2f {
	private float x;
	private float y;
	
	public Vector2f (float x, float z) {
		this.x = x;
		this.y = z;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float z) {
		this.y = z;
	}
	
}
